lados = int(input('Digite a quantidade de lados: '))
if lados == 1:
    print('PONTO')
elif lados == 2:
    print('LINHA')
elif lados == 3:
    print('TRIÂNGULO')
elif lados == 4:
    print('QUADRILÁTERO')
elif lados == 5:
    print('PENTÁGONO')
elif lados == 6:
    print('HEXÁGONO')
else:
    print('Você está certo disso? ')
